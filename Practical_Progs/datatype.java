public class datatype {
    int x;
    byte b;
    float f;
    boolean bl;
    char c;
    public static void main(String[] args) {
        datatype d = new datatype();
        System.out.println(d.x);
        System.out.println(d.b);
        System.out.println(d.f);
        System.out.println(d.bl);
        System.out.println(d.c);
    }
}